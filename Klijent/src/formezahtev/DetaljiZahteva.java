    private void btnIZmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIZmeniActionPerformed

        int row = tblStavke.getSelectedRow();

        if (row >= 0) {
            ModelTabeleStavkeKlijentska mt = (ModelTabeleStavkeKlijentska) tblStavke.getModel();
            StavkaZahteva sz = mt.getSelectedStavka(row);
            if (sz.getStatus().equals("Nije izvrseno")) {
                try {
                    sz.setStatus("Izvrseno");
                    boolean uspesno = klijentkontroler.KlijentKontroler.getInstance().updateZahtev(sz);
                    if (uspesno) {
                        JOptionPane.showMessageDialog(this, "Uspesno izmenjen zahtev!");
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Neuspesno izmenjen zahtev!");
                    }
                } catch (Exception ex) {
                    Logger.getLogger(DetaljiZahteva.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ova stavka je izvrsena!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Morate odabrati stavku!");
        }


    }//GEN-LAST:event_btnIZmeniActionPerformed


    private void srediFormu() {
        txtRok.setText(z.getRokZaIzvrsenje());
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        txtDatum.setText(sdf.format(z.getDatum()));
        cmbZap.getModel().setSelectedItem(z.getZaposleniKreator());
        cmbZaposleni.getModel().setSelectedItem(z.getZaposleniIzvrsitelj());
        txtDatum.setEnabled(false);
        txtRok.setEnabled(false);
        cmbZap.setEnabled(false);
        cmbZaposleni.setEnabled(false);
        tblStavke.setModel(new ModelTabeleStavkeKlijentska(z));
        proveri();
    }

    private void proveri() {
        ModelTabeleStavkeKlijentska mt = (ModelTabeleStavkeKlijentska) tblStavke.getModel();
        int brojStavki = mt.getLista().size();
        int brojIzvrsenih = 0;
        for (StavkaZahteva stavka : mt.getLista()) {
            if (stavka.getStatus().equals("Izvrseno")) {
                brojIzvrsenih++;
            }
        }
        if (brojStavki == brojIzvrsenih) {
            lbl2.setText("U ovom zahtevu, sve stavke su izvrsene!");
            tblStavke.setEnabled(false);
            btnIZmeni.setEnabled(false);
        }
    }

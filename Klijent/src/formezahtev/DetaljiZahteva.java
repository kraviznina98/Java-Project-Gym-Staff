    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ModelTabeleStavkeKlijentska mt = (ModelTabeleStavkeKlijentska) tblStavke.getModel();
        z.setStavke(mt.getLista());
        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da obriste zahtev?", "Konfirmacija", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            try {
                boolean uspesno = KlijentKontroler.getInstance().deleteZahtev(z);
                if (uspesno) {
                    JOptionPane.showMessageDialog(this, "Uspesno obrisan zahtev!");
                    Zahtevi zah = (Zahtevi) getParent();
                    zah.refreshTable();
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this,"Neuspesno obrisan zahtev!");
                }
            } catch (Exception ex) {
                Logger.getLogger(DetaljiZahteva.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed


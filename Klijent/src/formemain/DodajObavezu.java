    private void popuniObaveze() {
        try {
            ArrayList<Obaveza> obaveze=KlijentKontroler.getInstance().getAllObaveza();
            cmbObaveza.removeAllItems();
            
            for (Obaveza obaveza : obaveze) {
                cmbObaveza.addItem(obaveza);
            }
        } catch (Exception ex) {
            Logger.getLogger(DodajObavezu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

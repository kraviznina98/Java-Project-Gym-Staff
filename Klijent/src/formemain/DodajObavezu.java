    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(txtOpus.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Opis je obavezno polje!");
            return;
        }
        Obaveza o=(Obaveza) cmbObaveza.getSelectedItem();
        String opis=txtOpus.getText();
        String status="";
        if(rbIzvrseno.isSelected()){
            status="Izvrseno";
        }
        else if(rbNijeIzvrseno.isSelected()){
            status="Nije izvrseno";
        }
        
        StavkaZahteva sz=new StavkaZahteva(-1, null, opis, status, o);
        KlijentskaForma kf=(KlijentskaForma) getParent();
        
        if(!kf.postoji(sz)){
            kf.dodaj(sz);
        }else{
            JOptionPane.showMessageDialog(this,"Vec postoji ova obaveza!");
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        
        try {
            if(txtIme.getText().isEmpty()
                    || txtKorisnickoIme.getText().isEmpty()
                    || txtPassword.getPassword().equals("")
                    || txtPrezime.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"Sva polja su obavezna!");
                return;
            }
            String ime=txtIme.getText();
            String username=txtKorisnickoIme.getText();
            String password=String.copyValueOf(txtPassword.getPassword());
            String prezime=txtPrezime.getText();
            Opstina opstina=(Opstina) cmbOpstina.getSelectedItem();
            
            Zaposleni z=new Zaposleni(-1, ime, prezime, username, password, opstina);
            
            boolean uspesno=KlijentKontroler.getInstance().addZaposleni(z);
            
            if(uspesno){
                JOptionPane.showMessageDialog(this,"Uspesno kreiran zaposleni!");
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this,"Neuspesno kreiran zaposleni!");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed


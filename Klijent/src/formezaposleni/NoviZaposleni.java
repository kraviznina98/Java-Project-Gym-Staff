    private void popuniOpstine() {
       
        try {
            ArrayList<Opstina> opstine=KlijentKontroler.getInstance().getAllOpstina();
            
            cmbOpstina.removeAllItems();
            
            for (Opstina opstina : opstine) {
                cmbOpstina.addItem(opstina);
            }
        } catch (Exception ex) {
            Logger.getLogger(NoviZaposleni.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

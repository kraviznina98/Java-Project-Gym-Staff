    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (txtNaziv.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti parametar za pretragu!");
            return;
        }

        String parametar = txtNaziv.getText();

        ModelTabeleZaposleni mt = (ModelTabeleZaposleni) tblZaposleni.getModel();
        mt.setParametar(parametar);
        if (!poruka.equals("")) {
            lbl1.setText(poruka);
            lbl1.setVisible(true);
        } else {
            lbl1.setVisible(false);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


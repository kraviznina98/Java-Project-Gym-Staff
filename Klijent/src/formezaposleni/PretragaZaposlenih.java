    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int row = tblZaposleni.getSelectedRow();

        if (row >= 0) {
            ModelTabeleZaposleni mt = (ModelTabeleZaposleni) tblZaposleni.getModel();
            Zaposleni z = mt.getSelectedZaposleni(row);
            new DetaljiZaposlenog(this, true, z).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Morate odabrati obavezu!");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

